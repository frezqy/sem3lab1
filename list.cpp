    Node* node = new Node{val, nullptr}; 
    if(!list.head) // Проверка, пуст ли список.
    {
        list.head = node; // Если пуст, новый узел становится головой.
        return;
    }
    
    Node* cur = list.head; // Создаем указатель для обхода, начинаем с головы.
//проверка на узел, если есть (не нулевой указатель), то сдвигаем вперед
