// Функция инициализации односвязного списка. Сложность O(1).
void initList(MyList &list)
{
    list.head = nullptr; // Установка указателя на голову (начало) в null (список пуст).
}

// Функция освобождения динамически выделенной памяти (полная очистка списка). Сложность O(N).
void freeList(MyList &list)
{
    Node* cur = list.head; // Начинаем с головы списка.
    while(cur != nullptr) // Обходим список, пока не дойдем до конца.
    {
        Node* tmp = cur; // Сохраняем текущий узел во временную переменную.
        cur = cur->next; // Переходим к следующему узлу (чтобы не потерять его после удаления).
        delete tmp; // Освобождаем память, занятую старым узлом.
    }
    list.head = nullptr; // После удаления всех узлов голова должна стать null.
}

