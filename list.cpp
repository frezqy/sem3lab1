// Операция удаления элемента из хвоста списка. Сложность O(N) в односвязном списке.
void delTail(MyList &list)
{
    if(!list.head) // Случай 1: Список пуст.
    {
        cout << "Список пуст" << endl;
        return;
    }
    if(!list.head->next) // Случай 2: В списке только один элемент.
    {
        delete list.head; // Удаляем его.
        list.head = nullptr; // Голова становится null.
        return;
    }

    Node* cur = list.head; // Начинаем с головы.
    // Ищем узел, ПРЕДШЕСТВУЮЩИЙ последнему (тот, чей 'next->next' равен nullptr).
    while(cur->next->next) 
        cur = cur->next; // Сдвигаемся вперед.
    
    delete cur->next; // Освобождаем память последнего узла (cur->next).
    cur->next = nullptr; // Новый последний узел (cur) теперь указывает на nullptr.
}

// Операция удаления элемента по заданному значению. Сложность O(N).
void delByValue(MyList &list, const string& val) {
    if(!list.head) { // Случай 1: Список пуст.
        cout << "Список пуст." << endl;
        return;
    }
    if(list.head->value == val) { // Случай 2: Удаляем голову.
        delHead(list);
        cout << "Элемент " << val << " удален." << endl;
        return;
    }
    Node* cur = list.head; // Начинаем с головы.
    // Ищем узел, ПРЕДШЕСТВУЮЩИЙ целевому узлу.
    while(cur->next && cur->next->value != val) {
        cur = cur->next;
    }
    if(!cur->next) { // Если цикл завершился, но целевой узел не найден.
        cout << "Элемент " << val << " не найден." << endl;
        return;
    }
    // Если элемент найден (cur->next - это целевой узел):
    Node* tmp = cur->next; // Сохраняем указатель на удаляемый узел.
    cur->next = tmp->next; // Пропускаем удаляемый узел (связываем cur с узлом после tmp).
    delete tmp; // Освобождаем память удаляемого узла.
    cout << "Элемент " << val << " удален." << endl;
}

// Операция поиска элемента по значению. Сложность O(N).
void findValue(const MyList &list, const string& val) {
    Node* curr = list.head; // Начинаем с головы.
    int p = 0; // Счетчик позиции.
    while(curr) { // Обходим список.
        if(curr->value == val) { // Если элемент найден.
            cout << "Элемент \"" << val << "\" найден на позиции: " << p << endl;
            return; // Завершаем поиск.
        }
        curr = curr->next; // Сдвигаемся вперед.
        p++; // Увеличиваем позицию.
    }
    cout << "Элемент \"" << val << "\" не найден." << endl; // Если цикл завершился, но элемент не найден.
}

// Операция прямого чтения (с головы до хвоста). Сложность O(N).
void readForward(const MyList &list)
{
    if(!list.head) // Проверка на пустой список.
    {
        cout << "Список пуст" << endl;
        return;
    }
    cout << "Список вперёд";
    for(Node* curr = list.head; curr; curr= curr->next) // Обход списка с помощью 'next'.
    {
        cout << curr->value << " "; // Вывод значения.
    }
    cout << endl;
}

// Вспомогательная рекурсивная функция для обратного вывода.
void printBack(Node* node)
{
    if(!node) // Базовый случай рекурсии: если узел null, выходим.
        return;
    printBack(node->next); // Рекурсивный вызов: сначала вызываем для СЛЕДУЮЩЕГО узла (движемся до конца).
    cout << node -> value << " "; // Вывод: значение узла выводится только после возврата из рекурсии (LIFO).
}

// Операция обратного чтения (с хвоста до головы) с использованием рекурсии. Сложность O(N).
void readBack(const MyList &list)
{
    if(!list.head) // Проверка на пустой список.
    {
        cout << "Список пуст" << endl;
        return;
    }
    cout << "Список (назад)";
    printBack(list.head); // Вызов рекурсивной функции, начиная с головы.
    cout << endl;
}

// Вспомогательная функция удаления головы. Сложность O(1).
void delHead(MyList &list) {
    if (!list.head) return; // Если список пуст, выходим.
    Node* tmp = list.head; // Сохраняем указатель на текущую голову.
    list.head = list.head->next; // Смещаем голову на следующий узел.
    delete tmp; // Освобождаем память старой головы.
}

// Операция удаления узла, расположенного СРАЗУ ПОСЛЕ узла со значением 'val'. Сложность O(N).
void delAfterValue(MyList &list, const string& val) {
    if (!list.head || !list.head->next) { // Проверка на пустой список или список с 1 элементом.
        cout << "Нечего удалять после элемента." << endl;
        return;
    }
    Node* cur = list.head; // Начинаем с головы.
    while(cur && cur->value != val) { // Ищем целевой узел.
        cur = cur->next;
    }
    if (!cur || !cur->next) { // Если целевой узел не найден или он последний.
        cout << "Элемент '" << val << "' не найден или он последний." << endl;
        return;
    }
    // Если элемент найден (cur) и за ним что-то есть (cur->next):
    Node* tmp = cur->next; // Узел, который нужно удалить.
    cur->next = tmp->next; // Пропускаем удаляемый узел.
    delete tmp; // Освобождаем память.
    cout << "Элемент после '" << val << "' удален." << endl;
}


// Операция удаления узла, расположенного СРАЗУ ПЕРЕД узлом со значением 'val'. Сложность O(N).
void delBeforeValue(MyList &list, const string& val) {
    if (!list.head || !list.head->next) { // Проверка на пустой список или список с 1 элементом.
        cout << "Нечего удалять перед элементом." << endl;
        return;
    }
    // Случай 1: Удаление узла, стоящего перед вторым элементом (то есть удаление головы).
    if (list.head->next->value == val) { 
        delHead(list);
        cout << "Элемент перед '" << val << "' удален." << endl;
        return;
    }
    Node* cur = list.head; // Начинаем с головы.
    // Ищем узел, ПРЕДШЕСТВУЮЩИЙ узлу, который мы собираемся удалить.
    // Условие: cur->next->next->value == val.
    while(cur->next && cur->next->next && cur->next->next->value != val) {
        cur = cur->next;
    }
    if (!cur->next || !cur->next->next) { // Если целевой элемент не найден или перед ним нечего удалять (слишком близко к голове).
        cout << "Элемент '" << val << "' не найден или перед ним нечего удалять." << endl;
        return;
    }
    // Если элемент найден (cur->next - удаляемый узел):
    Node* tmp = cur->next; // Узел, который нужно удалить.
    cur->next = tmp->next; // Пропускаем удаляемый узел.
    delete tmp; // Освобождаем память.
    cout << "Элемент перед '" << val << "' удален." << endl;
}