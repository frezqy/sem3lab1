#include "queue.h"
#include <iostream>
#include <string>

// Функция инициализации очереди. Сложность O(1).
void init(Queue &q)
{
    q.front = nullptr; // Указатель на начало очереди (голову) устанавливается в null.
    q.rear = nullptr;  // Указатель на конец очереди (хвост) устанавливается в null.
}

// Проверка на пустоту очереди. Сложность O(1).
bool isEmpty(const Queue& q)
{
    return q.front == nullptr; // Очередь пуста, если указатель на начало равен null.
}

// Операция добавления элемента в конец очереди (PUSH/ENQUEUE). Сложность O(1).
void push(Queue& q,std::string val)
{
    QNode* newNode = new QNode; // Выделение памяти для нового узла.
    newNode ->data = val;       // Присвоение значения новому узлу.
    newNode->next = nullptr;    // Новый узел всегда становится последним, его 'next' = null.

    if(isEmpty(q)) // Если очередь пуста.
    {
        q.front = newNode; // Новый узел становится и началом,
        q.rear = newNode;  // и концом очереди.
    }
    else // Если очередь не пуста.
    {
        q.rear->next = newNode; // Текущий последний узел указывает своим 'next' на новый узел.
        q.rear = newNode;       // Новый узел становится новым концом очереди.
    }
}

//удаление элемента из начала очереди
// Операция извлечения элемента из начала очереди (POP/DEQUEUE). Сложность O(1).
std::string pop(Queue &q) {
    if(isEmpty(q)) { // Проверка на пустую очередь.
        return ""; // Возвращаем пустую строку, если очередь пуста.
    }
    QNode* tmp = q.front;          // Сохранение указателя на первый элемент (для удаления).
    std::string value = tmp->data; // Сохранение значения, которое нужно вернуть.

    q.front = q.front->next; // Сдвиг указателя 'front' на следующий элемент (удаление элемента).
    delete tmp;              // Освобождение памяти, занятой старым первым элементом.

    if(q.front == nullptr) { // Если после удаления очередь стала пустой.
        q.rear = nullptr;    // Указатель на конец также должен стать null.
    }
    return value; // Возвращение извлеченного значения.
}

// Операция чтения первого элемента очереди без удаления (PEEK/FRONT). Сложность O(1).
std::string peek(const Queue &q)
{
    if(isEmpty(q)) // Проверка на пустую очередь.
    {
        return ""; // Возвращение пустой строки.
    }
    return q.front->data; // Возвращение значения первого элемента.
}

// Операция полной очистки очереди. Сложность O(N) (так как вызывает pop N раз).
void clear(Queue &q)
{
    while(!isEmpty(q)) // Пока очередь не пуста.
    {
        pop(q); // Извлекаем и удаляем элементы по одному.
    }
}

// Функция вывода всех элементов очереди. Сложность O(N).
void print(const Queue& q) {
    if (isEmpty(q)) {
        std::cout << "Очередь пуста." << std::endl;
        return;
    }
    QNode* current = q.front; // Начинаем обход с начала очереди.
    std::cout << "Очередь: ";
    while(current != nullptr) { // Обход до конца.
        std::cout << current->data << " "; // Вывод значения.
        current = current->next;          // Переход к следующему элементу.
    }
    std::cout << std::endl;
}