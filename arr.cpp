#include <iostream>
#include <string>
#include "arr.h"

using namespace std;

// Функция инициализации структуры массива (вызывается при старте программы).
void initArray(MyArr &arr) {
    arr.data = nullptr; // Инициализация указателя на данные нулевым значением (nullptr).
    arr.size = 0;       // Установка текущего размера массива в ноль.
}

// Функция освобождения динамически выделенной памяти (вызывается при завершении).
void freeArr(MyArr &arr) {
    delete[] arr.data;  // Освобождение памяти, на которую указывает arr.data (используется delete[] для массивов).
    arr.data = nullptr; // Обнуление указателя во избежание "висячих" указателей.
    arr.size = 0;       // Сброс размера массива в ноль.
}

// Функция принимает значение как параметр
// Операция добавления элемента в конец массива.
void addEnd(MyArr &arr, const string& val) {
    // Выделение новой памяти, которая на 1 элемент больше текущего размера.
    string* newData = new string[arr.size + 1]; 
    // Цикл для копирования всех старых элементов из arr.data в новый массив newData.
    // Сложность этой операции O(N), так как нужно пройти по всем N элементам.
    for(int i = 0; i < arr.size; i++) {
        newData[i] = arr.data[i];
    }
    // Добавление нового значения в конец нового массива (по индексу, равному старому размеру).
    newData[arr.size] = val; 
    // Освобождение старой памяти, которую занимал arr.data.
    delete[] arr.data; 
    // Переназначение указателя arr.data на новый, больший массив.
    arr.data = newData; 
    // Увеличение счетчика размера массива на единицу.
    arr.size++; 
}

// Функция принимает индекс как параметр
// Операция получения (чтения) элемента по индексу.
void getAt(const MyArr &arr, int idx) {
    // Проверка на некорректный индекс (выход за границы массива).
    // Сложность проверки O(1).
    if (idx < 0 || idx >= arr.size) { 
        cout << "Ошибка: Некорректный индекс." << endl; // Вывод сообщения об ошибке.
        return; // Выход из функции.
    }
    // Вывод элемента по заданному индексу.
    // Операция доступа O(1) – мгновенный переход по индексу.
    cout << "Элемент по индексу " << idx << ": \"" << arr.data[idx] << "\"" << endl; 
}

// Операция добавления элемента по заданному индексу.
void addAt(MyArr &arr, int idx, const string& val) {
    // Проверка на некорректный индекс (индекс может быть от 0 до arr.size включительно).
    if (idx < 0 || idx > arr.size) { 
        cout << "Ошибка: Некорректный индекс." << endl; // Вывод ошибки.
        return; // Выход.
    }
    // Выделение новой памяти для массива, который на 1 элемент больше.
    string* newData = new string[arr.size + 1]; 
    // Цикл для копирования элементов ДО индекса вставки.
    for(int i = 0; i < idx; i++) { 
        newData[i] = arr.data[i];
    }
    // Вставка нового значения на заданный индекс.
    newData[idx] = val; 
    // Цикл для копирования элементов ПОСЛЕ индекса вставки со сдвигом на +1.
    // Общая сложность операции O(N) из-за необходимости копирования/сдвига N элементов.
    for(int i = idx; i < arr.size; i++) { 
        newData[i+1] = arr.data[i];
    }
    // Освобождение старой памяти.
    delete[] arr.data; 
    // Переназначение указателя на новый массив.
    arr.data = newData; 
    // Увеличение размера массива.
    arr.size++; 
    cout << "Элемент добавлен по индексу " << idx << "." << endl; // Уведомление об успешной операции.
}

// Функция принимает индекс как параметр
// Операция удаления элемента по индексу.
void delAt(MyArr &arr, int idx) {
    // Проверка на некорректный индекс (выход за границы массива).
    if (idx < 0 || idx >= arr.size) { 
        cout << "Ошибка: Некорректный индекс." << endl; // Вывод ошибки.
        return; // Выход.
    }
    // Выделение новой памяти для массива, который на 1 элемент меньше.
    string* newData = new string[arr.size - 1]; 
    // Цикл для копирования элементов ДО индекса удаления.
    for(int i = 0; i < idx; i++) { 
        newData[i] = arr.data[i];
    }
    // Цикл для копирования элементов ПОСЛЕ индекса удаления со сдвигом на -1 (пропуск удаляемого элемента).
    // Общая сложность операции O(N) из-за необходимости копирования/сдвига N элементов.
    for(int i = idx + 1; i < arr.size; i++) { 
        newData[i-1] = arr.data[i];
    }
    // Освобождение старой памяти.
    delete[] arr.data; 
    // Переназначение указателя на новый, меньший массив.
    arr.data = newData; 
    // Уменьшение размера массива.
    arr.size--; 
    cout << "Элемент по индексу " << idx << " удален." << endl; // Уведомление об успешной операции.
}

// Функция вывода всего содержимого массива.
void readArray(const MyArr &arr) {
    // Проверка, пуст ли массив.
    if (arr.size == 0) { 
        cout << "Массив пуст." << endl; // Вывод сообщения о пустом массиве.
        return; // Выход.
    }
    cout << "Массив: "; // Начало вывода массива.
    // Цикл для прохода по всем элементам массива.
    // Сложность O(N), так как нужно просмотреть все элементы.
    for(int i = 0; i < arr.size; i++) { 
        cout << "\"" << arr.data[i] << "\" "; // Вывод каждого элемента.
    }
    cout << endl; // Перенос строки в конце.
}

// замена элемента по индексу
// Операция замены элемента по индексу.
void repArr(MyArr &arr, int idx, const string& val) {
    // Проверка на некорректный индекс (выход за границы массива).
    if (idx < 0 || idx >= arr.size) { 
        cout << "Ошибка: Некорректный индекс." << endl; // Вывод ошибки.
        return; // Выход.
    }
    arr.data[idx] = val; // Прямая замена значения в существующем массиве по индексу.
    // Сложность операции O(1).
    cout << "Элемент по индексу " << idx << " заменен." << endl; // Уведомление.
}

